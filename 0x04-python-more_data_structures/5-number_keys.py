#!/usr/bin/python3
def number_keys(a_dictionary):
    """
    Returns the number of keys in a dictionary.

    Args:
    - a_dictionary: A dictionary object.

    Returns:
    - The number of keys in the dictionary.

    Example:
    >>> my_dict = {'a': 1, 'b': 2, 'c': 3}
    >>> number_keys(my_dict)
    3
    """
    return len(a_dictionary)
